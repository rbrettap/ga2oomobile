package com.winit.ga2oo.objects;

public class UpcomingEvent 
{
	public int resImgId;
	public int friendImg1;
	public int friendImg2;
	public String title;
	
	public UpcomingEvent(int resImgId, int friendImg1, int friendImg2, String title)
	{
		this.resImgId = resImgId;
		this.friendImg1 = friendImg1;
		this.friendImg2 = friendImg2;
		this.title = title;
	}
}
